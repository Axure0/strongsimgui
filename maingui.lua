local function autofarm() 
    local lp = game.Players.LocalPlayer
    while wait() do
        if getgenv().AutoFarmEnabled then
	    local oldval = game.Players.LocalPlayer.leaderstats["💪Strength"].Value
            local args = {
                [1] = "S_Tools_Activate",
                [2] = {}
            }

            game:GetService("ReplicatedStorage"):WaitForChild("Common"):WaitForChild("Library"):WaitForChild("Network"):WaitForChild("RemoteFunction"):InvokeServer(unpack(args))
            
	    if game.Players.LocalPlayer.leaderstats["💪Strength"].Value == oldval then
		local args = {
                    [1] = "S_Tools_Toggle",
                    [2] = {
                        [1] = "Weight4"
                    }
                }

                game:GetService("ReplicatedStorage"):WaitForChild("Common"):WaitForChild("Library"):WaitForChild("Network"):WaitForChild("RemoteEvent"):FireServer(unpack(args))
	    end
	end
    end
end
local function music()
    while wait() do
        if getgenv().MusicEnabled == false then
            game:GetService("Workspace").Music.Playing = false
        elseif getgenv().MusicEnabled and game:GetService("Workspace").Music.Playing == false then
            game:GetService("Workspace").Music.Playing = true
        end
    end
end
local function autorebirth()
    while wait() do
        if getgenv().AutoRebirthEnabled then
            if game:GetService("Players").LocalPlayer.PlayerGui.Main.Menus.Rebirth.RebirthBtn.BackgroundColor3 ~= Color3.new(150, 161, 166) then
                local args = {
                    [1] = "S_Rebirth_Request",
                    [2] = {}
                }
                
                game:GetService("ReplicatedStorage").Common.Library.Network.RemoteFunction:InvokeServer(unpack(args))
            end
        end
    end
end
local function autokill()
    local chars = game:GetService("Workspace").Characters
    while wait() do
	if getgenv().AutoKillEnabled then
	   if getgenv().AutoFarmEnabled then
		getgenv().UIToggle(game:GetService("Players").LocalPlayer.PlayerGui.ScreenGui.Frame.Frame.Frame.Frame.Main.Frame.AutoFarm, "AutoFarmEnabled")
	   end

	   local oldyframe = chars[game.Players.LocalPlayer.Name].HumanoidRootPart.CFrame.Y
	
	   for i,v in pairs(game:GetService("Players"):GetChildren()) do
		if v ~= game.Players.LocalPlayer then
		    function inarea(Part,Character)
		    	local touching = Part:GetTouchingParts()
			for i=1,#touching do
			   if touching[i] == Character.HumanoidRootPart then
			      return true
			   end
			end
			return false
		    end
				
		    if inarea(game:GetService("Workspace").SafeZone.Sensor, v.Character) == false then
		        chars[game.Players.LocalPlayer.Name].HumanoidRootPart.CFrame = CFrame.new(Vector3.new(chars[v.Name].HumanoidRootPart.CFrame.X, oldyframe, chars[v.Name].HumanoidRootPart.CFrame.Z))
		    end
		end
	   end
	end
    end
end

